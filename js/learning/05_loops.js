console.log("Loops in JS");

/**
 * LOOPS
 * loops run the code multiple times
 * loops are of three types
 * - for loop
 * - while loop
 * - do while loop
 * loop need 3 things to run
 * - initialization
 * - condition 
 * - increment/decrement
 */




for (let index = 0; index < 10; index++) {
    console.log(index + " => Hello Sir, How are you");
}

