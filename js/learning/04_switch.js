let age = 10;


switch (age) {
    case 10:
        console.log("you are 10 years old");
        break;
    case 12:
        console.log("you are 12 , you are getting ready for high school");
        break;
    case 16:
        console.log("you are 16 , getting ready for college");
        break;
    default:
        console.log("you are not 10,12,or 16");
        break;
}

